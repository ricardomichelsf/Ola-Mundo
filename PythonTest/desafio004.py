n = input("Digite algo : ")
print(type(n))
print('É \033[1;7;30malfanumérico?\033[m'.format(n.isalnum()))
print('É \033[1;7;30malfabético?\033[m',n.isalpha())
print('É \033[1;7;30mdecimal?\033[m',n.isdecimal())
print('É um \033[1;7;30mdigito?\033[m', n.isdigit())
print('Esta \033[1;7;30mcapitalizado?\033[m', n.istitle())
print('É um \033[1;7;30mnúmero?\033[m', n.isnumeric())
print('Esta \033[1;7;30mimprimivel?\033[m',n.isprintable())
print('Esta em \033[1;7;30mminúscula?\033[m', n.islower())
print('Esta em \033[1;7;30mmaiúscula?\033[m', n.isupper())
print('Esta com \033[1;7;30mespaço?\033[m', n.isspace())
